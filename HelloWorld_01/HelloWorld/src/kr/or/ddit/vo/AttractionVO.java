package kr.or.ddit.vo;

public class AttractionVO {
	private String attraction_photo;
	private String attraction_video;
	private String attraction_name;
	private String attraction_region;
	private int attraction_no;
	
	public String getAttraction_photo() {
		return attraction_photo;
	}
	public void setAttraction_photo(String attraction_photo) {
		this.attraction_photo = attraction_photo;
	}
	public String getAttraction_video() {
		return attraction_video;
	}
	public void setAttraction_video(String attraction_video) {
		this.attraction_video = attraction_video;
	}
	public String getAttraction_name() {
		return attraction_name;
	}
	public void setAttraction_name(String attraction_name) {
		this.attraction_name = attraction_name;
	}
	public String getAttraction_region() {
		return attraction_region;
	}
	public void setAttraction_region(String attraction_region) {
		this.attraction_region = attraction_region;
	}
	public int getAttraction_no() {
		return attraction_no;
	}
	public void setAttraction_no(int attraction_no) {
		this.attraction_no = attraction_no;
	}
	
	
	
}
